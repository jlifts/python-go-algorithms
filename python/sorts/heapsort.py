def heapsortUnique(arr):
    n = len(arr)

    for i in range(n // 2 - 1, -1, -1):
        heapify(arr, n, i)

    for i in range(n-1, 0, -1):
        arr[i], arr[0] = arr[0], arr[i]
        heapify(arr, i, 0)
        temp = []
        for i in arr:
            if i not in temp:
                temp.append(i)
        arr = temp
        return arr


